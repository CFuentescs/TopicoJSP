package modelo;

/**
 *
 * @author Alumno
 */
public class carga {
      
    
    public carga() {

    }

    public String rut_carga;
    public String nombre;
    public String fecha_nac;
    public String genero;
    public String rut_empleado;
    public String bono_escolar;
    public String bono_navidad;

    public String getRut_carga() {
        return rut_carga;
    }

    public String getNombre() {
        return nombre;
    }

    public String getFecha_nac() {
        return fecha_nac;
    }

    public String getGenero() {
        return genero;
    }

    public String getRut_empleado() {
        return rut_empleado;
    }

    public String getBono_escolar() {
        return bono_escolar;
    }

    public String getBono_navidad() {
        return bono_navidad;
    }

    public void setRut_carga(String rut_carga) {
        this.rut_carga = rut_carga;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setFecha_nac(String fecha_nac) {
        this.fecha_nac = fecha_nac;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public void setRut_empleado(String rut_empleado) {
        this.rut_empleado = rut_empleado;
    }

    public void setBono_escolar(String bono_escolar) {
        this.bono_escolar = bono_escolar;
    }

    public void setBono_navidad(String bono_navidad) {
        this.bono_navidad = bono_navidad;
    }
    
    
}
